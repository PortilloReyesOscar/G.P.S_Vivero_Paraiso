package Modelo;

/**
 *
 * @author porti
 */
public class Persona {
    int id;
    String nom;
    String correo;

    public Persona(int id, String nom, String correo) {
        this.id = id;
        this.nom = nom;
        this.correo = correo;
    }

    public Persona() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getnom() {
        return nom;
    }

    public void setnom(String nom) {
        this.nom = nom;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }
    
    
}
